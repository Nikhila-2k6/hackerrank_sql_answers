SELECT DISTINCT CITY
FROM STATION
WHERE LOWER(CITY) LIKE 'a%a'
   OR LOWER(CITY) LIKE 'a%e'
   OR LOWER(CITY) LIKE 'a%i'
   OR LOWER(CITY) LIKE 'a%o'
   OR LOWER(CITY) LIKE 'a%u'
   OR LOWER(CITY) LIKE 'e%a'
   OR LOWER(CITY) LIKE 'e%e'
   OR LOWER(CITY) LIKE 'e%i'
   OR LOWER(CITY) LIKE 'e%o'
   OR LOWER(CITY) LIKE 'e%u'
   OR LOWER(CITY) LIKE 'i%a'
   OR LOWER(CITY) LIKE 'i%e'
   OR LOWER(CITY) LIKE 'i%i'
   OR LOWER(CITY) LIKE 'i%o'
   OR LOWER(CITY) LIKE 'i%u'
   OR LOWER(CITY) LIKE 'o%a'
   OR LOWER(CITY) LIKE 'o%e'
   OR LOWER(CITY) LIKE 'o%i'
   OR LOWER(CITY) LIKE 'o%o'
   OR LOWER(CITY) LIKE 'o%u'
   OR LOWER(CITY) LIKE 'u%a'
   OR LOWER(CITY) LIKE 'u%e'
   OR LOWER(CITY) LIKE 'u%i'
   OR LOWER(CITY) LIKE 'u%o'
   OR LOWER(CITY) LIKE 'u%u';
